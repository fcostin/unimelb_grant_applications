def dump_shortlist(s, n):
    
    def sign(x):
        if x > 0:
            return 1
        elif x < 0:
            return -1
        else:
            return 0
    
    lines = s.split('\n')
    lines = [x.strip() for x in lines]
    lines = [x for x in lines if x != '']
    pairs = [x.split() for x in lines]
    pairs = [(x[0], float(x[1])) for x in pairs]
    pairs.sort(cmp = lambda x, y : sign(x[1] - y[1]), reverse = True)
    return pairs[:n]

